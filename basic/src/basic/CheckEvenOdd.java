package basic;

public class CheckEvenOdd {
	public static void main(String[] args) {
		int num = 20;
		/* If number is divisible by 2 then its an even numer 
		 * else odd number */
		if ( num % 2 == 0)
			System.out.println("Enter the number is even");
		else 
			System.out.println("Enter the number is odd");
			
	
}
}
